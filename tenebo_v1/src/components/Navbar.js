import Dropdown from "./Dropdown"
import "./Navbar.css"
import { Link } from "react-router-dom"
import { useEffect, useState } from "react"
import { useLocation } from "react-router"

function Navbar () {
  const [ dropdown, setDropdown ] = useState(false)
  const [ connected, toggleConnect ] = useState(false)
  const location = useLocation()
  const [ , updateAddress ] = useState("0x")
  const [ navbar, setNavbar ] = useState(false)


  async function getAddress () {
    const ethers = require("ethers")
    const provider = new ethers.providers.Web3Provider(window.ethereum)
    const signer = provider.getSigner()
    const addr = await signer.getAddress()
    updateAddress(addr)
  }

  function updateButton () {
    const ethereumButton = document.querySelector(".enableEthereumButton")
    ethereumButton.textContent = "Connected"
    ethereumButton.classList.remove("hover:bg-blue-70")
    ethereumButton.classList.remove("bg-blue-500")
    ethereumButton.classList.add("hover:bg-green-70")
    ethereumButton.classList.add("bg-green-500")
  }

  async function connectWebsite () {
    const chainId = await window.ethereum.request({ method: "eth_chainId" })
    if (chainId !== "0x5") {
      //alert('Incorrect network! Switch your metamask network to Rinkeby');
      await window.ethereum.request({
        method: "wallet_switchEthereumChain",
        params: [ { chainId: "0x5" } ],
      })
    }
    await window.ethereum
      .request({ method: "eth_requestAccounts" })
      .then(() => {
        updateButton()
        console.log("here")
        getAddress()
        window.location.replace(location.pathname)
      })
  }

  const handleDropdown = () => {
    setDropdown(!dropdown)
  }

  useEffect(() => {
    if (window.ethereum.isConnected()) {
      getAddress()
      toggleConnect(true)
      updateButton()
    }

    window.ethereum.on("accountsChanged", function (_accounts) {
      window.location.replace(location.pathname)
    })
  }, [ location.pathname ])

  return (


    <nav className="w-full bg-white shadow">
      <div className="justify-between px-4 mx-auto lg:max-w-7xl md:items-center md:flex md:px-8">
        <div>
          <div className="flex items-center justify-between py-3 md:py-5 md:block">
            <Link to="/"><h2 className="text-2xl font-bold">Tenebo</h2></Link>
            <div className="md:hidden">
              <button
                className="p-2 text-gray-700 rounded-md outline-none focus:border-gray-400 focus:border"
                onClick={() => setNavbar(!navbar)}
              >
                {navbar ? (
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    className="w-6 h-6"
                    viewBox="0 0 20 20"
                    fill="currentColor"
                  >
                    <path
                      fillRule="evenodd"
                      d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z"
                      clipRule="evenodd"
                    />
                  </svg>
                ) : (
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    className="w-6 h-6"
                    fill="none"
                    viewBox="0 0 24 24"
                    stroke="currentColor"
                    strokeWidth={2}
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M4 6h16M4 12h16M4 18h16"
                    />
                  </svg>
                )}
              </button>
            </div>
          </div>
        </div>
        <div>
          <div
            className={`flex-1 justify-self-center pb-3 mt-8 md:block md:pb-0 md:mt-0 ${navbar ? "block" : "hidden"}`}
          >
            <ul className="items-center justify-center space-y-8 md:flex md:space-x-6 md:space-y-0">
              <li className="text-gray-600 hover:text-blue-600">
                <Link to="/">Home</Link>
              </li>
              <li className="text-gray-600 hover:text-blue-600">
                <Link to="/adoptions">Adoptions</Link>
              </li>
              <li className="text-gray-600 hover:text-blue-600">
                <Link to="/sellNFT">Mint Predator</Link>
              </li>
              <li className="text-gray-600 relative hover:text-blue-600">
                <button
                  type="button"
                  className="inline-flex"
                  id="menu-button"
                  aria-expanded="true"
                  aria-haspopup="true"
                  onClick={handleDropdown}
                >
                  Mara
                </button>
                {dropdown && <Dropdown />}
              </li>
              <li className="text-gray-600 hover:text-blue-600">
                <Link to="/profile">Profile</Link>
              </li>
              <li className="text-gray-600 hover:text-blue-600">
                <button
                  className="enableEthereumButton bg-blue-500 hover:bg-blue-700 text-white font-bold py-2 px-4 rounded text-sm"
                  onClick={connectWebsite}
                >
                  {connected ? "Connected" : "Connect Wallet"}
                </button>
              </li>
            </ul>
          </div>
        </div>
      </div>
    </nav>
  )
}

export default Navbar
